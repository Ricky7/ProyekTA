<?php

namespace Sifo\AdminBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Kelas
 */
class Kelas
{
    /**
     * @var string
     */
    private $kodeKelas;

    /**
     * @var string
     */
    private $namaKelas;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set kodeKelas
     *
     * @param string $kodeKelas
     * @return Kelas
     */
    public function setKodeKelas($kodeKelas)
    {
        $this->kodeKelas = $kodeKelas;

        return $this;
    }

    /**
     * Get kodeKelas
     *
     * @return string 
     */
    public function getKodeKelas()
    {
        return $this->kodeKelas;
    }

    /**
     * Set namaKelas
     *
     * @param string $namaKelas
     * @return Kelas
     */
    public function setNamaKelas($namaKelas)
    {
        $this->namaKelas = $namaKelas;

        return $this;
    }

    /**
     * Get namaKelas
     *
     * @return string 
     */
    public function getNamaKelas()
    {
        return $this->namaKelas;
    }

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }
    /**
     * @var integer
     */
    private $tingkatKelas;


    /**
     * Set tingkatKelas
     *
     * @param integer $tingkatKelas
     * @return Kelas
     */
    public function setTingkatKelas($tingkatKelas)
    {
        $this->tingkatKelas = $tingkatKelas;

        return $this;
    }

    /**
     * Get tingkatKelas
     *
     * @return integer 
     */
    public function getTingkatKelas()
    {
        return $this->tingkatKelas;
    }
}
